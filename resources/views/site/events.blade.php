<h2>About Radix</h2>
   <p>
       <strong>Sustainable business, or green business</strong>, is an enterprise that has minimal negative impact on the global or local environment, community, society, or economy—a business that strives to meet the triple bottom line.</p>
       <p>
    Dimensions of sustainability – environmental, social, economic
    Facets of Sustainable Business Strategies:</p>
    <ul>
    <li>
        Corporate Sustainability: a business approach that creates long-term shareholder value by embracing opportunities and managing risks deriving from economic, environmental and social developments.</li>
        <li>
            Sustainable marketing: meeting business objectives and giving customers what they really need, with lower costs and less environmental impact.</li>
        <li>Sustainability marketing: building and maintaining sustainable relationships with customers, the social environment and the natural environment.</li>
        <li>Green Marketing: focuses on market pull and legislative push towards improved, environmental friendly corporate performance</li> 
        <li>Financial Sustainability Strategies: not only includes money but also other types of resources one might obtain, such as in-kind support, volunteer staff, or shared resources from other organizations and convincing another organization to continue on-going projects.</li>
        <li>SOM (Sustainable Operations Management) includes topics such as green supply chain, green procurement and reverse logistics</li>
        <li>Importance of HRM in Sustainability: HR involvement around sustainability tends to be greatest in areas such as development (both leadership development and training), talent management, ethics and diversity, and employee engagement.</li> 
        <li>Sustainable Business through Social Entrepreneurship: Sometimes sustainable business is manifested in the activities of startups and small businesses that pursue double and triple bottom lines</li> 
    </ul>
    <p><strong>Applied Engineering Management </strong>is the managing, implementing and directing of engineering technology from the point of inception by the research department to the finished product. </p>
